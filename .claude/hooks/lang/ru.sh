#!/usr/bin/env bash
# @cc-only-rationale: language pack (payload prose) for the two internal reminder hooks — not shipped to consumer projects via install.sh
# @dual-pair: hook-lang-i18n
#
# Russian payload pack for end-of-turn-reminder.sh + ask-question-reminder.sh.
# Sourced by the hooks when AIF_HOOK_LANG=ru. Each aif_msg_* function emits one
# reminder body; ${anchor} is resolved at call time from the hook's scope.
# Companion: lang/en.sh (canonical default). Key parity enforced by
# lang/check-parity.sh. See docs/superpowers/specs/2026-06-01-hook-lang-i18n-design.md.
#
# shellcheck disable=SC2154  # ${anchor} is assigned by the sourcing hook (dynamic scope), not here.

# Recap heading the end-of-turn hook greps for (already-recapped guard) AND embeds
# in the recap instruction. Guard ↔ message stay consistent because both read this.
AIF_RECAP_MARKER='## 🟢 Простыми словами'

# Russian phrasings of the trailing-fork detector (sibling of en.sh; same key).
# Category-3 match-data: matches the operator-facing turn text, which is Russian
# when AIF_HOOK_LANG=ru. Used by end-of-turn-reminder.sh Branch B.
AIF_EOT_QUESTION_PATTERN='выбирай|реши сам|какой (вариант|подход)|выбери|хочешь чтобы'

# Fallback value for the session-goal anchor when extraction fails.
aif_msg_eot_anchor_fallback() {
  printf '%s' '(цель сессии не извлеклась — назови её сам)'
}

# PreToolUse:AskUserQuestion — pre-question fork-challenge (no interpolation;
# quoted heredoc because the body contains backticks).
aif_msg_question_challenge() {
  cat <<'EOF'
Стоп — ты собираешься задать вопрос. Сначала проверь сам вопрос, в первую очередь для себя.
1. Это настоящая развилка — или ты перекладываешь решение, которое можешь принять сам? Если один вариант явно лучше по существу (по целям сессии и дисциплине проекта) — НЕ спрашивай: сделай его и скажи, что сделал.
2. Если это правда развилка — сначала ТВОЯ обоснованная рекомендация: «Рекомендую <вариант>, потому что <причина против целей и трейдоффов>», потом альтернативы коротко. Решает человек.
3. Простыми словами: что именно решаем и почему это блокирует — на конкретном примере, не повтор текста вопроса.
4. Если это развилка о ДИЗАЙНЕ/СТРАТЕГИИ (а не быстрый A/B по фактам) — открой `superpowers:brainstorming` вместо голой карточки: исследуй → порекомендуй с аргументами, потом спрашивай. Карточка по дизайн-форку читается как «AI punted».
Если всё это уже сделано в твоём ответе — просто задай вопрос снова: повтор не блокируется.
EOF
}

# Stop hook — Branch C: long answer AND trailing fork-question.
aif_msg_eot_branch_c() {
  cat <<EOF
Стоп. Это и длинный ответ, И вопрос-развилка в конце — нужен и пересказ работы, и проверка вопроса. В первую очередь для себя.
ОБЯЗАТЕЛЬНО начни блок ровно со строки «${AIF_RECAP_MARKER}» — чтобы человек опознал его с ходу.
(Не можешь сказать просто, конкретно и в общем — что/зачем/почему/как → сам не до конца понял; это диагностика, не отчёт.)

Цель сессии (из названия сессии / первого задания): «${anchor}».

Первые 2 строки — так, чтобы человек без контекста понял с ходу:
• Чем занята сессия В ЦЕЛОМ — одной фразой (не «что прям сейчас правил»).
• На той ли цели — выбери ОДНО: НА ЦЕЛИ / УВЕЛО В СТОРОНУ <тема> (и почему) / СОЗНАТЕЛЬНО СВЕРНУЛ на <тема> (и зачем). Не «вроде на цели».

Дальше — про всю сессию, НЕ про последний ход:
• Зачем мы это делаем — общая цель своими словами (не пересказ названия): какую задачу закрываем и почему она важна.
• Что уже сделано к этой цели — НАКОПИТЕЛЬНО, по шагам с именами (файл/PR/решение): всё значимое за сессию, а не только последнее изменение.
• Что впереди — ВСЁ что осталось до цели, по пунктам: не один следующий шаг, а весь хвост. Если близко к концу — так и скажи.
• В чём меньше всего уверен — одна вещь на перепроверку (или честно «таких нет»).
• Где сессия противоречит себе — назови разрыв между заявленной целью и тем, что вышло на деле, или место, где работа подрывает собственную предпосылку (как «проект против doc-bloat сам распух в doc-bloat»). Это «documents lie; tests don't», наведённое на сам пересказ. Нет настоящего разрыва — пропусти: натянутая ирония хуже её отсутствия.

По вопросу:
1. Это настоящая развилка — или я перекладываю решение, которое могу принять сам? Один вариант явно лучше по существу (по целям сессии и дисциплине) → НЕ спрашивай: сделай его и скажи, что сделал.
2. Если правда развилка — сначала МОЯ обоснованная рекомендация: «Рекомендую <вариант>, потому что <причина против целей и трейдоффов>». Потом альтернативы коротко. Решает человек.
Любой пункт не выходит конкретным → скажи прямо, не заполняй водой.
EOF
}

# Stop hook — Branch A: long substantive answer, no question (lighter per-turn recap).
aif_msg_eot_branch_a() {
  cat <<EOF
Стоп. Прежде чем закончить — пересказ простыми словами, в первую очередь для себя.
ОБЯЗАТЕЛЬНО начни блок ровно со строки «${AIF_RECAP_MARKER}» — чтобы человек опознал его с ходу.
(Не можешь сказать просто, конкретно и в общем — что/зачем/почему/как → сам не до конца понял; это диагностика, не отчёт.)

Цель сессии (из названия сессии / первого задания): «${anchor}».

Первые 2 строки — так, чтобы человек без контекста понял с ходу:
• Чем я сейчас занят — одной фразой, простым языком.
• На той ли я цели — выбери ОДНО: НА ЦЕЛИ / УВЕЛО В СТОРОНУ <тема> (и почему) / СОЗНАТЕЛЬНО СВЕРНУЛ на <тема> (и зачем). Не «вроде на цели».

Дальше для себя, по пункту, с именами (файл/функция/решение), без воды:
• Что я только что сделал — конкретное изменение, не пересказ задачи.
• Нетривиальные решения — «выбрал X вместо Y потому что Z», или честно «решений не было».
• В чём меньше всего уверен — назови ОДНУ вещь, которую стоит перепроверить.
• Следующий шаг и почему он следующий.
• Если в этом ходе ты что-то рекомендовал, или сказал «решай ты» / «жду твоего решения» / «PR готов, ждёт твой клик» — проверь себя: альтернативы реально перебраны или взял первое что пришло? Если по существу есть явно лучший вариант (по целям и дисциплине) — НЕ перекладывай, сделай и скажи что сделал. Передача решения = резерв для настоящих развилок.
• Обратное: решил ли ты в этом ходе развилку МОЛЧА — прямым действием/командой/диспатчем, не вынеся её вопросом? Если она неоднозначна (нет явно лучшего по мерилам проекта) — это #fork-decided-by-silent-action: вынеси её СЕЙЧАС через AskUserQuestion, не оставляй решённой молча. Оператор должен видеть и открытые, и закрытые развилки.
Любой пункт не выходит конкретным → скажи прямо, не заполняй водой.
EOF
}

# Stop hook — Branch B: a question with no long answer body (fork-challenge).
aif_msg_eot_branch_b() {
  cat <<EOF
Ты остановился на вопросе. Прежде чем ждать — проверь сам вопрос, для себя в первую очередь.
ОБЯЗАТЕЛЬНО начни блок ровно со строки «${AIF_RECAP_MARKER}».

Цель сессии: «${anchor}».

1. Это настоящая развилка — или я перекладываю решение, которое могу принять сам? Если один вариант явно лучше по существу (по целям сессии и дисциплине проекта) — НЕ спрашивай: сделай его и скажи, что сделал. Вопрос резервируй для развилок, где честно не выбрать на мерилах.
2. Если это правда развилка — сначала МОЯ обоснованная рекомендация: «Рекомендую <вариант>, потому что <причина против целей и трейдоффов>». Потом альтернативы коротко. Решает человек.
3. Простыми словами: что именно решаем и почему это блокирует — на простом примере, не повтор текста вопроса.
Суть выбора не объясняется просто → сам вопрос сформулирован неточно: скажи об этом.
EOF
}

# Заголовок story-пересказа (story-ветка Stop-хука + скил /story).
AIF_STORY_MARKER='## 🎬 Как это было'

# Story-ветка Stop-хука / скил /story: живой пересказ сессии простыми словами, когда
# работа сдана (запушен PR). Hook-style: вся инструкция локализована, поэтому язык
# вывода следует активному паку. ${anchor:-} безопасен при unset (у /story нет
# transcript-anchor — цель называет сам из контекста).
aif_msg_eot_branch_story() {
  cat <<EOF
Работа сдана (только что запушен PR) — расскажи человеку историю всей сессии, в первую очередь для него.
ОБЯЗАТЕЛЬНО начни блок ровно со строки «${AIF_STORY_MARKER}» — чтобы человек опознал его с ходу.

Цель сессии (из названия / первого задания): «${anchor:-(назови сам из контекста)}».

Рассказывай как историю, простым и живым языком — НЕ сухим чеклистом:
• Открой одной фразой — что вообще затевали и зачем, по-человечески.
• По актам — сюжетная арка ключевых ходов, с именами (файл / PR / решение): что делали, что пошло не так, как починили.
• Жаргон объясняй на месте — встретил термин (egress, caffeinate, Docker) → тут же аналогия одной строкой.
• Будь честен — где проверено слабо (1 прогон, 1 случай), в чём меньше всего уверен, что осталось.
• Кончи на человеке — одна вещь, которую решить/сделать ему («один шаг — твоё „го"»).
Тон: интересно, как история; без воды и само-поздравления; правда важнее гладкости. Если пункт не выходит конкретным — скажи прямо.
EOF
}
